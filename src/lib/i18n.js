function detectLocale() {
  if (!browser) return 'en';
  if (typeof localStorage !== 'undefined') {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === 'zh' || saved === 'en') return saved;
  }
  if (typeof navigator !== 'undefined' && navigator.language) {
    const lang = navigator.language.toLowerCase();
    if (lang.startsWith('zh')) return 'zh';
  }
  return 'en';
}

export const locale = writable(detectLocale());

export function setLocale(lang) {
  if (lang !== 'zh' && lang !== 'en') return;
  locale.set(lang);
  if (typeof localStorage !== 'undefined') localStorage.setItem(STORAGE_KEY, lang);
  if (typeof document !== 'undefined') {
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  }
}

export function t(key) {
  const keys = key.split('.');
  const dict = translations[get(locale)] || translations.en;
  let obj = dict;
  for (const k of keys) {
    obj = obj?.[k];
    if (obj === undefined) return key;
  }
  return typeof obj === 'string' ? obj : key;
}

/**
 * 按固定语言取文案（供预渲染 / SEO，不依赖浏览器下的 locale store）
 * @param {'en' | 'zh'} lang
 * @param {string} key
 */
export function tLang(lang, key) {
  const keys = key.split('.');
  const dict = translations[lang === 'zh' ? 'zh' : 'en'];
  let obj = dict;
  for (const k of keys) {
    obj = obj?.[k];
    if (obj === undefined) return key;
  }
  return typeof obj === 'string' ? obj : key;
}

